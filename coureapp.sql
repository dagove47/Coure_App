-- Crea una secuencia para generar automáticamente IDs de reserva
CREATE SEQUENCE reservaciones_seq START WITH 1 INCREMENT BY 1;

-- Crea la tabla de reservaciones
CREATE TABLE reservaciones (
    reserva_id NUMBER PRIMARY KEY,
    nombre VARCHAR2(50) NOT NULL,
    fecha DATE NOT NULL  
);

-- Crea un trigger para insertar automáticamente el ID de reserva
CREATE OR REPLACE TRIGGER tr_reservaciones
BEFORE INSERT ON reservaciones
FOR EACH ROW
BEGIN
    SELECT reservaciones_seq.NEXTVAL
    INTO :NEW.reserva_id
    FROM dual;
END;
/


/// Insertar usuario 

CREATE OR REPLACE PROCEDURE insertar_usuario(
    p_id INT,
    p_nombre VARCHAR2,
    p_apellido VARCHAR2,
    p_correo_electronico VARCHAR2,
    p_contrasena VARCHAR2
)
AS
BEGIN
    INSERT INTO usuarios(id, nombre, apellido, correo_electronico, contrasena)
    VALUES (p_id, p_nombre, p_apellido, p_correo_electronico, p_contrasena);
    COMMIT;
END insertar_usuario;
/


///Actualizar un usuario 

CREATE OR REPLACE PROCEDURE actualizar_usuario(
    p_id INT,
    p_nombre VARCHAR2,
    p_apellido VARCHAR2,
    p_correo_electronico VARCHAR2,
    p_contrasena VARCHAR2
)
AS
BEGIN
    UPDATE usuarios
    SET nombre = p_nombre,
        apellido = p_apellido,
        correo_electronico = p_correo_electronico,
        contrasena = p_contrasena
    WHERE id = p_id;
    COMMIT;
END actualizar_usuario;
/

///Eliminar un usuario 

CREATE OR REPLACE PROCEDURE eliminar_usuario(p_id INT)
AS
BEGIN
    DELETE FROM usuarios WHERE id = p_id;
    COMMIT;
END eliminar_usuario;
/

